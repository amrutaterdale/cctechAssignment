package frame;

public class image {

	int id;
	String img;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	@Override
	public String toString() {
		return "image [id=" + id + ", img=" + img + "]";
	}

	
	

	
}
